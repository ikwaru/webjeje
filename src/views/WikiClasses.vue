                                            </li>

                                            <li><strong>Uncanny Dodge</strong>. When an attacker that you can see hits
                                                you with an attack, you can use your reaction to halve the attack’s
                                                damage against you.</li>
                                        </ul>
                                        <h4>Beast Master</h4>
                                        <p>The Beast Master archetype embodies a friendship between the civilized races
                                            and the beasts of the world. United in focus, beast and ranger work as one
                                            to fight the monstrous foes that threaten civilization and the wilderness
                                            alike. Emulating the Beast Master archetype means committing yourself to
                                            this ideal, working in partnership with an animal as its companion and
                                            friend.

                                        </p>
