                                            <h4>Music and Magic</h4>
                                            <p>In the worlds of D&D, words and music are not just vibrations of air, but
                                                vocalizations with power all their own. The bard is a master of song,
                                                speech, and the magic they contain. Bards say that the multiverse was
                                                spoken into existence, that the words of the gods gave it shape, and
                                                that echoes of these primordial Words of Creation still resound
                                                throughout the cosmos. The music of bards is an attempt to snatch and
                                                harness those echoes, subtly woven into their spells and powers.</p>

                                            <p>The greatest strength of bards is their sheer versatility. Many bards
                                                prefer to stick to the sidelines in combat, using their magic to inspire
                                                their allies and hinder their foes from a distance. But bards are
                                                capable of defending themselves in melee if necessary. using their magic
                                                to bolster their swords and armor. Their spells lean toward charms and
                                                illusions rather than blatantly destructive spells. They have a
                                                wide-ranging knowledge of many subjects and a natural aptitude that lets
                                                them do almost anything well. Bards become masters of the talents they
                                                set their minds to perfecting, from musical performance to esoteric
                                                knowledge.</p>
