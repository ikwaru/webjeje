                                            <p>True bards are not common in the world. Not every minstrel singing in a
                                                tavern or jester cavorting in a royal court is a bard. Discovering the
                                                magic hidden in music requires hard study and some measure of natural
                                                talent that most troubadours and jongleurs lack. It can be hard to spot
                                                the difference between these performers and true bards, though. A bard’s
                                                life is spent wandering across the land gathering lore, telling stories,
                                                and living on the gratitude of audiences, much like any other
                                                entertainer. But a depth of knowledge, a level of musical skill, and a
                                                touch of magic set bards apart from their fellows.</p>
                                            <p>Only rarely do bards settle in one place for long, and their natural
                                                desire to travel—to find new tales to tell, new skills to learn, and new
                                                discoveries beyond the horizon—makes an adventuring career a natural
                                                calling. Every adventure is an opportunity to learn, practice a variety
                                                of skills, enter long-forgotten tombs, discover lost works of magic,
                                                decipher old tomes, travel to strange places, or encounter exotic
                                                creatures. Bards love to accompany heroes to witness their deeds
                                                firsthand. A bard who can tell an awe-inspiring story from personal
                                                experience earns renown among other bards. Indeed, after telling so many
                                                stories about heroes accomplishing mighty deeds, many bards take these
                                                themes to heart and assume heroic roles themselves.</p>
