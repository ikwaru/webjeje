                                                their natural form.</p>
                                            <h4>Preserve the Balance</h4>
                                            <p>For druids, nature exists in a precarious balance. The four elements that
                                                make up a world—air, earth, fire,
                                                and water—must remain in equilibrium. If one element were to gain power
                                                over the others, the world could be destroyed, drawn into one of the
                                                elemental planes and broken apart into its component elements. Thus,
                                                druids oppose cults of Elemental Evil and others who promote one element
                                                to the exclusion of others.
                                            </p>
