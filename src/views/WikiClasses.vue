                                                made with disadvantage.
                                            </li>

                                            <li><strong>Multiattack Defense</strong>. When a creature hits you with an
                                                attack, you gain a +4 bonus to AC against all subsequent attacks made by
                                                that creature for the rest of the turn.
                                            </li>

                                            <li><strong>Steel Will</strong>. You have advantage on saving throws against
                                                being frightened.</li>
                                        </ul>
