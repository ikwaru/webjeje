                                                with spells that harm and hinder foes. They can provoke awe and dread,
                                                lay curses of plague or poison, and even call down flames from heaven to
                                                consume their enemies. For those evildoers who will benefit most from a
                                                mace to the head, clerics depend on their combat training to let them
                                                wade into melee with the power of the gods on their side.

