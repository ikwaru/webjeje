                                                priests are called to a simple life of temple service, carrying out
                                                their gods’ will through prayer and sacrifice, not by magic and strength
                                                of arms. In some cities, priesthood amounts to a political office,
                                                viewed as a stepping stone to higher positions of authority and
                                                involving no communion with a god at all. True clerics are rare in most
                                                hierarchies.
                                            </p>
