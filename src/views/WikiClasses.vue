                                        <p>The ideal of the ranger has two classic expressions: the Hunter and the Beast
                                            Master.</p>
                                        <h4>Hunter</h4>
                                        <p>Emulating the Hunter archetype means accepting your place as a bulwark
                                            between civilization and the terrors of the wilderness. As you walk the
                                            Hunter’s path, you learn specialized techniques for fighting the threats you
                                            face, from rampaging ogres and hordes of orcs to towering giants and
                                            terrifying dragons.

                                        </p>
