                                                her god demands it. Pursuing the goals of the gods often involves
                                                braving dangers beyond the walls of civilization, smiting evil or
                                                seeking holy relics in ancient tombs. Many clerics are also expected to
                                                protect their deities’ worshipers, which can mean fighting rampaging
                                                orcs, negotiating peace between warring nations, or sealing a portal
                                                that would allow a demon prince to enter the world.

                                                Most adventuring clerics maintain some connection to established temples
                                                and orders of their faiths. A temple might ask for a cleric’s aid, or a
                                                high priest might be ina position to demand it. </p>
